public class C01_Comperasion {
    public static void main(String[] args) {

        int a=33;
        int b=41;
        System.out.println("a eşitmidir b ye :"+(a==b));//false cevabını verir
        System.out.println("a eşitmidir b ye :"+(a!=b));//true cevabını verir
        System.out.println("a eşitmidir b ye :"+(a>b));//false cevabını verir
        System.out.println("a eşitmidir b ye :"+(a<b));//true cevabını verir




    }
}
